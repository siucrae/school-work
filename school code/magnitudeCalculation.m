% Calculating the magnitude of an array x = [a, b, c], magnitude = sqrt(a^2 + b^2 + c^2)

% Variable initialization
a = 23.5;
b = 12.8;
c = 3.6;

% Magnitude calculation
magnitude = sqrt(a^2 + b^2 + c^2);

% Output message
disp('Calculating the magnitude of x');
disp('The values for a, b, c, are as follows:');
disp(a);
disp(b);
disp(c);
disp('The magnitude of the vector x = [a, b, c] is:');
disp(magnitude);
